package com.github.containersolutions.operator.sample;

import java.util.List;

public class Stores {
    private List<String> configstore;
    private List<String> filestore;
    
    public List<String> getConfigstore() {
        return configstore;
    }
    
    public void setConfigstore(List<String> configstore) {
        this.configstore = configstore;
    }
    
    public List<String> getFilestore() {
        return filestore;
    }
    
    public void setFilestore(List<String> filestore) {
        this.filestore = filestore;
    }
}
